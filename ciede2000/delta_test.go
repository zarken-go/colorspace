package ciede2000

import (
	"testing"

	"github.com/stretchr/testify/assert"

	"github.com/zarken-go/colorspace"
)

const (
	assertDelta = 1e-4
)

func assertDeltaE(t *testing.T, L1, A1, B1, L2, A2, B2, Expected float64) {
	assert.InDelta(t, Expected, DeltaE(
		colorspace.Lab{L: L1, A: A1, B: B1},
		colorspace.Lab{L: L2, A: A2, B: B2},
	), assertDelta)
}

